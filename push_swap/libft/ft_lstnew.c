#include "libft.h"

t_list	*ft_lstnew(int content)
{
	t_list	*self;

	self = (t_list *)malloc(sizeof(t_list));
	if (!self)
		return (NULL);
	self -> content = content;
	self -> next = NULL;
	self -> prev = NULL;
	return (self);
}

#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*last;
	t_list	*tail;

	last = ft_lstlast(*lst);
	tail = (last->next);
	if (!last)
	{
		*lst = new;
		return ;
	}
	new->next = tail;
	new->prev = last;
	tail->prev = new;
	last->next = new;
}
